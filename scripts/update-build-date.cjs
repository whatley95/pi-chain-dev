#!/usr/bin/env node
/**
 * Updates src/build-date.ts with the current UTC ISO timestamp.
 * Safe to run as a git pre-commit hook (CommonJS, no ESM imports).
 * When invoked as a git hook, it also stages the updated file.
 */

const { writeFileSync, existsSync } = require("node:fs");
const { join } = require("node:path");
const { spawnSync } = require("node:child_process");

const repoRoot = process.cwd();
const targetPath = join(repoRoot, "src", "build-date.ts");
const now = new Date().toISOString();

const content = `// Auto-generated by scripts/update-build-date.js.\n// Do not edit manually.\nexport const BUILD_DATE = "${now}";\n`;

if (!existsSync(join(repoRoot, "src"))) {
  console.error(`src/ directory not found at ${repoRoot}; skipping build date update.`);
  process.exit(1);
}

writeFileSync(targetPath, content, "utf-8");
console.log(`Updated build date: ${now}`);

// If we are running as a git hook, stage the updated build-date file.
if (process.env.GIT_DIR || process.env.GIT_INDEX_FILE) {
  const git = spawnSync("git", ["add", targetPath], { cwd: repoRoot, stdio: "ignore" });
  if (git.status === 0) {
    console.log("Staged src/build-date.ts");
  } else {
    console.warn("Failed to stage src/build-date.ts");
  }
}
