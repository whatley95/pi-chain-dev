#!/usr/bin/env node
/**
 * Updates src/build-date.ts with the current UTC ISO timestamp.
 * Intended to run as a git pre-commit hook.
 */

import { writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const targetPath = join(__dirname, "..", "src", "build-date.ts");
const now = new Date().toISOString();

const content = `// Auto-generated by scripts/update-build-date.js (git pre-commit hook).\n// Do not edit manually.\nexport const BUILD_DATE = "${now}";\n`;

writeFileSync(targetPath, content, "utf-8");
console.log(`Updated build date: ${now}`);
